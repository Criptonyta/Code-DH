// Dato del tipo number

let edad = 33;
console.log(edad);

// Dato del tipo nmmber

let precio = 50.5;
console.log(precio);

// Dato del tipo string con comillas dobles

let nombre = "Nacho";
console.log(nombre);

// Dato del tipo string con comillas simples

let apellido = 'Navaza';
console.log(apellido);

// Template string (simplifica la forma en la que se concatenan strings)

console.log(`Mi nombre es ${nombre} y mi apellido es ${apellido}`);

// Dato de tipo booleano con valor verdadero

let verdadero = true;
console.log(verdadero);

// Dato de tipo booleano con valor falso

let falso = false;
console.log(falso);

// Dato especial de tipo 'Not a number' (NaN)

let notANumber = NaN;
console.log(notANumber);

// Dato especial de tipo 'Null'

let nulo = null;
console.log(nulo);

// Dato especial de tipo Undefined

let sinValor;
console.log(sinValor);

// Objeto literal {clave: valor}

let persona = {
    nombre: 'Daniel',
    edad: 33
};
console.log(persona);

// Array Se diferencia del objeto literal ya que no usa clave, sino que usa el numero de orden dentro del array, llamado indice.

let paises = ['Argentina', 'Venezuela', 'Colombia']
console.log(paises);
