// Operadores de suma

console.log (5+4);

// Operadores de resta

console.log (5-4);

// Operadores de multiplicacion

console.log (5*4);

// Operadores de division

console.log (5/4);

// Operadores de Modulo (el resultado es el resto de una division)

console.log (5%4);

// Operadores de incremento

let numero = 6;
console.log (++numero);

// Operadores de decremento

let otroNumero = 6;
console.log (--otroNumero);

// Operadores de comparacion simple (== solo compara el Valor). El resultado es siempre un booleano

console.log (5=='5');

// Operadores de comparacion estricta (=== compara el Valor y tipo de dato). El resultado es siempre un booleano

console.log (5==='5');

// Operadores de desigualdad simple (== solo compara el Valor). El resultado es siempre un booleano

console.log (5!='5');

// Operadores de desigualdad estricta (=== compara el Valor y tipo de dato). El resultado es siempre un booleano

console.log (5!=='5');

// Mayor que. El resultado es un booleano

console.log (5>6);

// Mayor o igual que. El resultado es un booleano

console.log (5>=6);

// Menor que. El resultado es un booleano

console.log (5<6);

// Menor o igual que. El resultado es un booleano

console.log (5<=6);

// AND &&

console.log(true && true);

// OR ||

console.log(true||true);

// Concatenacion

let nombre = 'Daniel';
let apellido = 'Navaza';
console.log(nombre+' '+apellido);

// Template string (simplifica la forma en la que se concatenan strings)

console.log(`Mi nombre es ${nombre} y mi apellido es ${apellido}`);
